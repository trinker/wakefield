function (n, x = datasets::state.name, prob = wakefield::state_populations[["Proportion"]],
    name = "State") {

    if (missing(n)) stop("`n` is missing")
    out <- sample(x = x, size = n, replace = TRUE, prob = prob)
    out <- factor(out, levels = x)
    varname(out, name)

}


